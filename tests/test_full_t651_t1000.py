@pytest.mark.parametrize("tid,text", _BLOCKED, ids=[x[0] for x in _BLOCKED])
async def test_logging_blocked(tid, text):
    async with AsyncClient(transport=ASGITransport(app=app), base_url=BASE) as c:
        status, body = await ask(c, text)
    assert status == 400, f"{tid} must be blocked (generates security audit event): {body}"


@pytest.mark.parametrize("tid,text", _ALLOWED, ids=[x[0] for x in _ALLOWED])
async def test_logging_allowed(tid, text):
    async with AsyncClient(transport=ASGITransport(app=app), base_url=BASE) as c:
        status, body = await ask(c, text)
    assert status == 200, f"{tid} must be allowed (generates normal audit event): {body}"
